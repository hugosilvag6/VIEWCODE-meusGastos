import UIKit

extension UIColor {
   static var inputsColor: UIColor {
      return UIColor(named: "inputsColor") ?? UIColor.blue
   }
   static var outputsColor: UIColor {
      return UIColor(named: "outputsColor") ?? UIColor.red
   }
}
